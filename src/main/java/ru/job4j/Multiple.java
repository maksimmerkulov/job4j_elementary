package ru.job4j;

/**
 * Класс {@code Multiple} предназначен для вывода таблицы умножения числа 1 на
 * числа от 2 до 9.
 *
 * <p>Каждая строка выводит результат умножения 1 на очередное число в формате
 * {@code "1 * n = результат"}.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * System.out.println("1 * 2 = 2");
 * System.out.println("1 * 3 = 3");
 * System.out.println("1 * 4 = 4");
 * System.out.println("1 * 5 = 5");
 * System.out.println("1 * 6 = 6");
 * System.out.println("1 * 7 = 7");
 * System.out.println("1 * 8 = 8");
 * System.out.println("1 * 9 = 9");
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * 1 * 2 = 2
 * 1 * 3 = 3
 * 1 * 4 = 4
 * 1 * 5 = 5
 * 1 * 6 = 6
 * 1 * 7 = 7
 * 1 * 8 = 8
 * 1 * 9 = 9
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.2
 */
public class Multiple {

    /**
     * Точка входа в программу.
     *
     * <p>Выводит таблицу умножения для числа {@code 1} на числа от {@code 2}
     * до {@code 9}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        System.out.println("1 * 2 = 2");
        System.out.println("1 * 3 = 3");
        System.out.println("1 * 4 = 4");
        System.out.println("1 * 5 = 5");
        System.out.println("1 * 6 = 6");
        System.out.println("1 * 7 = 7");
        System.out.println("1 * 8 = 8");
        System.out.println("1 * 9 = 9");
    }
}
