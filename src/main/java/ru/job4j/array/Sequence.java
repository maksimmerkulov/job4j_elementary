package ru.job4j.array;

/**
 * Класс {@code Sequence} демонстрирует создание и инициализацию двумерного
 * массива размером 3x3.
 *
 * <p>Массив инициализируется числами от 1 до 9 в порядке слева направо
 * и сверху вниз.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * int[][] array = new int[3][3];
 * array[0][0] = 1; array[0][1] = 2; array[0][2] = 3;
 * array[1][0] = 4; array[1][1] = 5; array[1][2] = 6;
 * array[2][0] = 7; array[2][1] = 8; array[2][2] = 9;
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * Массив заполнен числами от 1 до 9:
 * 1 2 3
 * 4 5 6
 * 7 8 9
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.3
 */
public class Sequence {

    /**
     * Точка входа в программу.
     *
     * <p>Создает двумерный массив 3x3 и инициализирует его значениями
     * от 1 до 9 в порядке следования слева направо и сверху вниз.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        int[][] array = new int[3][3];
        array[0][0] = 1;
        array[0][1] = 2;
        array[0][2] = 3;
        array[1][0] = 4;
        array[1][1] = 5;
        array[1][2] = 6;
        array[2][0] = 7;
        array[2][1] = 8;
        array[2][2] = 9;
    }
}
