package ru.job4j.loop;

/**
 * Класс {@code Fitness} содержит метод для вычисления количества месяцев,
 * необходимых Ивану, чтобы обогнать Николая по силе тяги.
 *
 * <p>Задача: Иван и Николай начинают с разных значений силы тяги, и каждый
 * месяц сила Ивана увеличивается в 3 раза, а сила Николая — в 2 раза. Метод
 * {@link #calc(int, int)} вычисляет количество месяцев, за которые сила Ивана
 * превзойдет силу Николая.</p>
 *
 * <p><b>Пример использования:</b></p>
 * <pre>{@code
 * int result = Fitness.calc(10, 20);
 * }</pre>
 *
 * <p><b>Пример вывода:</b></p>
 * <pre>{@code
 * result = 2
 * }</pre>
 *
 * @author Maksim Merkulov
 * @version 1.2
 */
public class Fitness {

    /**
     * Вычисляет количество месяцев, которые нужны Ивану, чтобы обогнать
     * Николая по силе тяги.
     *
     * <p>Каждый месяц сила Ивана увеличивается в 3 раза, а сила Николая —
     * в 2 раза. Метод работает до тех пор, пока сила Ивана не станет больше
     * силы Николая.</p>
     *
     * @param ivan начальная сила Ивана (в кг)
     * @param nik начальная сила Николая (в кг)
     * @return количество месяцев, которые понадобятся Ивану, чтобы
     *         превзойти Николая
     */
    public static int calc(int ivan, int nik) {
        int month = 0;
        while (ivan <= nik) {
            ivan *= 3;
            nik *= 2;
            month++;
        }
        return month;
    }
}
