package ru.job4j.loop;

/**
 * Класс LoopFor демонстрирует работу цикла for.
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class LoopFor {

    /**
     * Основной метод программы. Выводит числа от 5 до 10 включительно.
     */
    public static void main(String[] args) {
        for (int i = 5; i <= 10; i++) {
            System.out.println(i);
        }
    }
}